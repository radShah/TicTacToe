package com.tictactoe.Interface;


/**
  * Represents a "piece" on a 3x3 tic-tac-toe {@link Board} or 3x3x3 {@link Board3D}.
  */
public enum Piece {
  /**
   * The "X" piece.
   */
  X,
  /**
   * The "O" piece.
   */
  O,
  /**
   * Represents an "open" position on the {@link Board} or {@link Board3D}.
   */
  OPEN
}
